
public class GreatestOfTwo {
    public static void main(String[] args){
        int num1 = 12;
        int num2 = 3;
        if(num1 == num2 ){
            System.out.println("Both are Equal");
        }        
        if(num1 > num2){
            System.out.println("num1 is greater ");
        }else{
            System.out.println("num2 is greater");
        }
    }
}
